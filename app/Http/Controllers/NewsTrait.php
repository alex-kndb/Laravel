    public function getNewsById(int $id) : array
    {
        $news = $this->getNews();
        if (!isset($news[$id])) {
            die("No such news (id: $id)");
        }

        return $news[$id];
    }

    public function getNewsByCategory(string $category) : array
    {
        $news = $this->getNews();
        $filtered = array_filter($news, function ($item) use ($category){
            return ($item['category'] === $category) ? $item : false;

        });
        return $filtered;
    }
